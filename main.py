# Boolean
is_student = False
for_sale = True
is_online = True

if is_student:
    print("You are a student")
else:
    print("You are NOT a student")

if for_sale:
    print("That item is for sale")
else:
    print("That item is NOT sale")

if is_online:
    print("You are online")
else:
    print("You are offline")